                        <p><b>FamilyKasih</b>, Depok - Saat anak demam, orangtua pasti akan mulai panik. Mereka mungkin akan langsung mengukur suhu tubuh anak. Melansir Kids Health, anak-anak sering mengalami demam, dan ini juga tidak melulu kondisi yang buruk.</p>
                        <p>Demam pada anak terjadi karena sistem kekebalan tubuh anak bertarung melawan penyakit. Jika suhu anak sudah cukup tinggi dan menunjukkan gejala lain, maka pemeriksaan di rumah sakit segera wajib dilakukan.</p>
                        <p>Untuk ibu yang telah memiliki anak, penting untuk mengetahui tentang cara mengukur suhu tubuh yang tepat dan berapa kisaran suhu yang perlu diwaspadai. Dengan begini, ibu paham kapan harus membawa anak ke dokter, atau kapan harus membiarkan anak untuk beristirahat. Simak cara-cara dalam mengukur suhu tubuh anak yang tepat saat demam berikut ini.</p>
                        <h3>Jenis Termometer yang Bisa Digunakan</h3>
                        <p>Pertama-tama, kamu harus mengetahui jenis termometer yang tepat untuk mengukur suhu tubuh anak saat demam, yaitu:</p>
                        <h4>Termometer Digital</h4>
                        <p>Termometer digital memberikan pembacaan tercepat, paling akurat, dan merupakan satu-satunya jenis yang saat ini direkomendasikan oleh dokter. Tersedia dalam berbagai ukuran dan bentuk, termometer ini dijual di sebagian besar supermarket dan toko obat. Kamu juga bisa beli lewat website <b>FamilyKasih</b> supaya lebih mudah.</p>
                        <p>Pastikan kamu membaca instruksi pada kemasan untuk melihat bagaimana cara menggunakannya dan berbagai informasi yang harus kamu ketahui. Area tubuh anak yang bisa diukur suhunya, adalah:</p>
                        <ul>
                            <li>Area Dubur. Pengukuran suhu di sini adalah metode akurat untuk anak di bawah 3 tahun;</li>
                            <li>Area Mulut. Metode ini cocok untuk anak-anak berusia 4-5 tahun atau lebih;</li>
                            <li>Area Lengan. Termometer digital ini memang paling tidak akurat, tetapi bagus untuk pemeriksaan pertama.</li>
                        </ul>
                        <h4>Jenis Termometer Lainnya</h4>
                        <p>Termometer arteri temporal - ini mengukur gelombang panas di sisi dahi dan dapat digunakan pada bayi 3 bulan ke atas;</p>
                        <p>Termometer telinga elektronik (timpani) - ini mengukur gelombang panas dari gendang telinga dan dapat digunakan pada bayi 6 bulan ke atas.</p>
                        <h4>Jenis Tidak Direkomendasikan</h4>
                        <p>Jenis-jenis termometer ini tidak direkomendasikan karena kurang akurat:</p>
                        <ul>
                            <li>Termometer strip plastik;</li>
                            <li>Termometer dot;</li>
                            <li>Aplikasi pengukur suhu pada smartphone;</li>
                            <li>Termometer merkuri kaca. Jenis termometer ini tidak disarankan karena merkuri berpotensi merusak lingkungan.</li>
                        </ul>
                        <h4>Kiat untuk Mengukur Suhu Tubuh Anak Berdasarkan Usia</h4>
                        <p>Seperti yang diketahui banyak orangtua, mengukur suhu tubuh anak yang aktif cenderung sulit dilakukan. Metode terbaik tergantung pada usia dan temperamen anak, di antaranya:</p>
                        <ul>
                            <li>Untuk bayi di bawah 3 bulan, kamu mendapatkan hasil pembacaan suhu tubuh paling tepat dengan menggunakan termometer digital untuk mengukur suhu dubur. Hubungi dokter jika bayi berusia di bawah 3 bulan memiliki 38 derajat Celcius atau lebih tinggi.</li>
                            <li>Untuk bayi berusia antara 3 bulan dan 6 bulan, termometer rektal digital masih merupakan pilihan terbaik. Termometer arteri temporal juga dapat digunakan.</li>
                            <li>Untuk anak-anak berusia antara 6 bulan dan 4 tahun, kamu bisa gunakan termometer digital untuk mengukur suhu dubur. Kamu dapat menggunakan termometer timpani (telinga) atau termometer digital untuk mengukur suhu ketiak, tetapi kurang akurat.</li>
                            <li>Untuk anak-anak berusia 4 tahun atau lebih, kamu bisa gunakan termometer digital untuk mengukur suhu secara oral jika anak cukup suportif. Tetapi anak-anak yang banyak batuk atau bernapas melalui mulut mereka karena hidung tersumbat tidak mampu menutup mulut mereka cukup lama untuk metode oral. Pada kasus ini, kamu bisa menggunakan metode temporal, timpani, dubur, atau aksila (dengan termometer digital).</li>
                        </ul>
                        <p>Itulah yang bisa dilakukan untuk mengukur suhu tubuh anak berdasarkan usianya. Selalu perhatikan setiap gejala yang terjadi pada anak dan lakukan pemeriksaan pada anak agar mendapatkan penanganan yang tepat.</p>
